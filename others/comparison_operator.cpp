#include<iostream>

using namespace std;

int main()
{
    int a = 5;
    int b = 4;

    cout<< (a<b) <<endl;  // less than
    cout<< (a>b) <<endl;  // greater than
    cout<< (a<=b) <<endl; // less than equal to
    cout<< (a>=b) <<endl; // greater than equal to
    cout<< (a==b) <<endl; // equal 
    cout<< (a!=b) <<endl; // not equal

    return 0;
}